package main

// +build ignore

import (
	"io/ioutil"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

const tpl = `// Code generated by go generate. DO NOT EDIT.
// sources:
{{ range $idx, $path := .Sources }}// {{ $path }}
{{ end }}
package {{ .Package }} // import "happydns.org/{{ .Directory }}"

var Icons = map[string][]byte{
{{ range $file, $content := .Map }}  {{printf "%q" $file}}: []byte({{printf "%q" $content}}),
{{ end }}}
`

var bundleTpl = template.Must(template.New("").Parse(tpl))

type valTpl struct {
	Directory string
	Package   string
	Sources   []string
	Map       map[string][]byte
}

func main() {
	dir := os.Args[1]
	pkg := os.Args[2]

	d := valTpl{
		Directory: dir,
		Package:   pkg,
		Sources:   []string{},
		Map:       map[string][]byte{},
	}

	err := filepath.Walk(dir, func(srcFile string, info os.FileInfo, err error) error {
		if filepath.Ext(srcFile) == ".png" {
			data, err := ioutil.ReadFile(srcFile)
			if err != nil {
				return err
			}

			d.Sources = append(d.Sources, srcFile)
			fPath := strings.Split(strings.TrimPrefix(strings.TrimSuffix(srcFile, ".png"), dir+"/"), "/")
			fPathLen := len(fPath) - 2
			if fPathLen < 0 {
				fPathLen = 0
			}
			d.Map[strings.Join(fPath[fPathLen:], ".")] = data
		}
		return nil
	})
	if err != nil {
		panic(err)
	}

	f, err := os.Create(dir + "/icons.go")
	if err != nil {
		panic(err)
	}
	defer f.Close()

	bundleTpl.Execute(f, d)
}
